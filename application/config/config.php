function sendyourbulksms($phone_numbers,$message,$trmplateid="")
{
    // API endpoint
    $url = "http://control.yourbulksms.com/api/sendhttp.php";
    $apiKey = "32336e6c696e6534393264";
    $sender = "ZUKUZZ";
    // Prepare data to be sent
    $data = array(
        "authkey" => $apiKey,
        "mobiles" => $phone_numbers,
        "sender" => $sender,        
        "route" => "2",
        "country" => "91",
        "message" => $message,
        "DLT_TE_ID" => $trmplateid == "" ? "1207170936823838010" : $trmplateid
    );

    // Initialize cURL session
    $ch = curl_init();

    // Set the cURL options
    curl_setopt($ch, CURLOPT_URL, $url);
    curl_setopt($ch, CURLOPT_POST, 1);
    curl_setopt($ch, CURLOPT_POSTFIELDS, http_build_query($data));
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);

    // Execute cURL session
    $response = curl_exec($ch);
    
    // Close cURL session
    curl_close($ch);
    return $response;
}
